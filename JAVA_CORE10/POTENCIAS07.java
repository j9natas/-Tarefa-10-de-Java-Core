package lista_10;



public class POTENCIAS07 {

	
	 public static void main(String[] args) {
	   
	        int expoente = 0;
	  
	        int resultado = 1;
	        
	        
	        while (expoente <= 15) {
	    
	            System.out.println("3^" + expoente + " = " + resultado);
	           
	            resultado *= 3;
	         
	            expoente++;
	        }
	    }
	}
	
	
	
	

